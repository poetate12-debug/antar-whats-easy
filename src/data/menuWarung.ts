export const getMenuByWarungId = (warungId: string): Menu[] => {
  return menuData.filter((m) => m.warungId === warungId && m.isAvailable);
};

export const getMenuById = (id: string): Menu | undefined => {
  return menuData.find((m) => m.id === id);
};

export const getMenusByKategori = (
  warungId: string
): Record<string, Menu[]> => {
  const menus = getMenuByWarungId(warungId);
  return menus.reduce((acc, menu) => {
    const kategori = menu.kategori || "Lainnya";
    if (!acc[kategori]) {
      acc[kategori] = [];
    }
    acc[kategori].push(menu);
    return acc;
  }, {} as Record<string, Menu[]>);
};
